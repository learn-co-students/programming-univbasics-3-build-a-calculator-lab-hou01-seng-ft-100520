def addition(num1, num2)
  puts "#{num1}"
  puts "#{num2}"
  sum = num1 + num2 
  puts sum
  sum
end

def subtraction(num1, num2)
  puts "#{num1}"
  puts "#{num2}"
  subtract = num1 - num2 
  puts subtract 
  subtract
end

def division(num1, num2)
  puts "#{num1}"
  puts "#{num2}"
  div = num1 / num2 
  puts div 
  div
end

def multiplication(num1, num2)
  puts "#{num1}"
  puts "#{num2}"
  multi = num1 * num2 
  puts multi
  multi 
end

def modulo(num1, num2)
  puts "#{num1}"
  puts "#{num2}"
  mod = num1 % num2 
  puts mod 
  mod
end

def square_root(num)
  puts "#{num}"
  Math.sqrt(num)
end
